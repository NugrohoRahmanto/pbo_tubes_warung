    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Database db = new Database();
            String namaMakanan = jTextField1.getText();
            int hargaMakanan = Integer.parseInt(jTextField2.getText());
            int stokMakanan = Integer.parseInt(jTextField3.getText());
            
            if (stokMakanan <= 0){
                JOptionPane.showMessageDialog(null,"Stok yang anda masukkan tidak valid","Error Tambah Makanan",JOptionPane.WARNING_MESSAGE);
            }else{
                String sql = "INSERT INTO `foods`( `namaMakanan`, `stokMakanan`, `hargaMakanan`, `kategori`) VALUES ('"+ namaMakanan +"','"+ stokMakanan +"','"+ hargaMakanan +"','"+ selectedKategori +"')";
                db.query(sql);
                db.close();
                JOptionPane.showMessageDialog(null,"Data berhasil ditambahkan","Tambah Makanan",JOptionPane.WARNING_MESSAGE);
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                loadDataKategori();
            }
  
        } catch (SQLException ex) {
            Logger.getLogger(AddMakanan.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddMakanan.class.getName()).log(Level.SEVERE, null, ex);
        } catch (HeadlessException | NumberFormatException ex) {
            Logger.getLogger(AddMakanan.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

