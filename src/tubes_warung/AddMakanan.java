    public final void loadDataKategori() throws SQLException, ClassNotFoundException{
        Database db = new Database();
        String sql = "SELECT `namaKategori` FROM `discounts`";
        ResultSet rs1 = db.getData(sql);
        
        jComboBox1.removeAllItems();
        
        while(rs1.next()){
            String namaKategori = rs1.getString("namaKategori");
            jComboBox1.addItem(namaKategori);
        }
        
        db.close();
    }

